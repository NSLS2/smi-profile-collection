# ---------------------------------------------------------------------------
# Foil layout: which physical foil is which material / thickness multiple.
# ---------------------------------------------------------------------------
# The 24 foils are 1x/2x/4x/8x stacks of six base materials.  ``multiple`` is the
# integer thickness factor relative to the BASE foil above (Beer-Lambert: the
# foil's optical depth is ``multiple * BASE_FOILS[base]['optical_depth']``).
#
# Keyed by the foil's short label ("1_3" == bank 1, position 3 == device ``att1_3``).
#: foil label -> (base-foil key, thickness multiple)
FOIL_LAYOUT = {
    # bank 1
    "1_1":  ("Cu_68um", 1), "1_2":  ("Cu_68um", 2), "1_3":  ("Cu_68um", 4), "1_4":  ("Cu_68um", 8),
    "1_5":  ("Sn_60um", 1), "1_6":  ("Sn_60um", 2), "1_7":  ("Sn_60um", 4), "1_8":  ("Sn_60um", 8),
    "1_9":  ("Sn_30um", 1), "1_10": ("Sn_30um", 2), "1_11": ("Sn_30um", 4), "1_12": ("Sn_30um", 8),
    # bank 2
    "2_1":  ("Mo_20um", 1), "2_2":  ("Mo_20um", 2), "2_3":  ("Mo_20um", 4), "2_4":  ("Mo_20um", 8),
    "2_5":  ("Al_102um", 1), "2_6": ("Al_102um", 2), "2_7": ("Al_102um", 4), "2_8": ("Al_102um", 8),
    "2_9":  ("Al_9um", 1), "2_10": ("Al_9um", 2), "2_11": ("Al_9um", 4), "2_12": ("Al_9um", 8),
}


def foil_description(label):
    """Human-readable description of one foil, e.g. ``'att1_3: 4x Cu 68um (272um Cu)'``."""
    base, mult = FOIL_LAYOUT[label]
    info = BASE_FOILS[base]
    formula = info["formula"]
    t = info["thickness_um"]
    total = mult * t
    return "att{}: {}x {} {:g}um ({:g}um {})".format(label, mult, formula, t, total, formula)


# ---------------------------------------------------------------------------
# Pure-physics layer (no ophyd / no EPICS) -- transmission & factor maths.
# Importable and unit-testable off the beamline.
# ---------------------------------------------------------------------------
import numpy as _np

_ENERGY_ARR = _np.asarray(ENERGY_EV, dtype=float)
#: smallest / largest tabulated energy (eV) -- queries are clamped to this range.
ENERGY_MIN_EV = float(_ENERGY_ARR[0])
ENERGY_MAX_EV = float(_ENERGY_ARR[-1])
#: per-base-foil optical-depth arrays (aligned to ``_ENERGY_ARR``), built once.
_OD_ARR = {k: _np.asarray(v["optical_depth"], dtype=float) for k, v in BASE_FOILS.items()}


def foil_optical_depth(label, energy_eV):
    """Optical depth ``-ln(T)`` of a single foil ``label`` at ``energy_eV``.

    Linear interpolation of OD vs energy (clamped to the tabulated range).  ``label``
    is a key of :data:`FOIL_LAYOUT` (e.g. ``"1_3"``).
    """
    base, mult = FOIL_LAYOUT[label]
    od = _np.interp(energy_eV, _ENERGY_ARR, _OD_ARR[base])
    return float(mult) * od


def combination_optical_depth(labels, energy_eV):
    """Total optical depth of the inserted-foil combination ``labels`` at ``energy_eV``.

    Optical depths add (transmissions multiply).  Empty combination -> 0 (no attenuation).
    """
    total = 0.0
    for label in labels:
        total += foil_optical_depth(label, energy_eV)
    return total


def transmission(labels, energy_eV):
    """Transmission (0-1) of the inserted-foil combination ``labels`` at ``energy_eV``."""
    return float(_np.exp(-combination_optical_depth(labels, energy_eV)))


def attenuation_factor(labels, energy_eV):
    """Attenuation factor ``1/T`` (>= 1) of the combination ``labels`` at ``energy_eV``.

    ``inf`` if the beam is fully absorbed (transmission underflows to 0).
    """
    t = transmission(labels, energy_eV)
    if t <= 0.0:
        return float("inf")
    return 1.0 / t


def select_foils(target_factor, energy_eV, candidates=None, max_foils=4,
                 tolerance=0.10, mode="closest"):
    """Choose a foil combination for ``target_factor``, favouring the **fewest foils**.

    More foils are harder to normalize and add scatter, so the search is biased toward
    small sets: it returns the *fewest* foils that land **within ``tolerance``** of the
    target, and only grows the set when a smaller one cannot get within tolerance.  If no
    combination up to ``max_foils`` reaches tolerance, it returns the closest match found
    (the caller is expected to warn and record the actual factor).

    Parameters
    ----------
    target_factor : float
        Desired attenuation factor (1 / transmission).  ``<= 1`` -> empty set (no foils).
    energy_eV : float
        Photon energy at which to evaluate the foils.
    candidates : iterable of str, optional
        Foil labels allowed in the search (default: all of :data:`FOIL_LAYOUT`).
    max_foils : int, optional
        Hard cap on how many foils may be combined (default 4).
    tolerance : float, optional
        Relative tolerance on the factor (default 0.10 = within 10%).  A combination is
        "good enough" if ``|achieved/target - 1| <= tolerance``.  Used to decide when a
        small foil set is acceptable so a larger, more-precise one is not needed.
    mode : {"closest", "atleast"}, optional
        ``"closest"`` (default): a combination counts as in-tolerance if within
        ``tolerance`` on either side.  ``"atleast"``: only combinations with
        ``factor >= target`` are acceptable (never less attenuation than requested); the
        in-tolerance test additionally requires ``factor <= target*(1+tolerance)``.

    Returns
    -------
    (labels, achieved_factor, within_tol) : (tuple[str], float, bool)
        The chosen foil labels (bank/position-sorted), the factor they actually give at
        ``energy_eV``, and whether that is within ``tolerance`` of the target.

    Notes
    -----
    Search is by increasing size (1 foil, then 2, ...): the first size at which an
    in-tolerance combination exists wins, taking the closest such combination at that
    size.  With 24 foils this evaluates at most a few thousand combinations and runs only
    when a *new* target is requested (not per data point).
    """
    import itertools

    if candidates is None:
        candidates = list(FOIL_LAYOUT.keys())
    candidates = list(candidates)

    # No attenuation requested.
    if target_factor <= 1.0:
        return (), 1.0, True

    log_target = _np.log(target_factor)
    # Precompute each candidate's OD at this energy (log(factor) == optical depth).
    od_at_e = {c: foil_optical_depth(c, energy_eV) for c in candidates}

    def _acceptable(factor):
        if mode == "atleast" and factor < target_factor:
            return False
        return abs(factor / target_factor - 1.0) <= tolerance

    # Track the globally closest combination (fallback if nothing is in tolerance), and
    # for "atleast" the strongest combination (fallback if nothing reaches the target).
    overall_best = None          # (cost, n, factor, labels)
    strongest = None             # (od, factor, labels)  for atleast fallback

    # Also seed with the empty set so a target ~1 is handled gracefully.
    def _track(labels, od):
        nonlocal overall_best, strongest
        factor = float("inf") if od >= 700 else float(_np.exp(od))
        cost = abs(od - log_target)
        n = len(labels)
        if overall_best is None or cost < overall_best[0] - 1e-12 or (
                abs(cost - overall_best[0]) <= 1e-12 and n < overall_best[1]):
            overall_best = (cost, n, factor, tuple(labels))
        if strongest is None or od > strongest[0]:
            strongest = (od, factor, tuple(labels))
        return factor

    for n in range(1, max_foils + 1):
        in_tol_at_this_size = None     # (cost, factor, labels)
        for combo in itertools.combinations(candidates, n):
            od = sum(od_at_e[c] for c in combo)
            factor = _track(combo, od)
            if _acceptable(factor):
                cost = abs(od - log_target)
                if in_tol_at_this_size is None or cost < in_tol_at_this_size[0]:
                    in_tol_at_this_size = (cost, factor, tuple(combo))
        if in_tol_at_this_size is not None:
            # Fewest foils that meet tolerance -> done; take the closest at this size.
            _, factor, labels = in_tol_at_this_size
            return tuple(sorted(labels, key=_foil_sort_key)), factor, True

    # Nothing within tolerance up to max_foils -> return the best (closest) fallback.
    if mode == "atleast" and strongest is not None and overall_best is not None \
            and overall_best[2] < target_factor:
        # nothing reached the target at all: use the strongest available set.
        _, factor, labels = strongest
    else:
        _, _, factor, labels = overall_best
    return tuple(sorted(labels, key=_foil_sort_key)), factor, False


def _foil_sort_key(label):
    """Sort foil labels naturally: bank then position (so '1_2' < '1_10')."""
    bank, _, pos = label.partition("_")
    return (int(bank), int(pos))
